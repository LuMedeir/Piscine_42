#include <unistd.h>

void	ft_print_comb(void)
{
	int	p[3];

	p[0] = '0';
	while (p[0] <= '7')
	{
		p[1] = p[0] + 1;
		while (p[1] <= '9')
		{
			p[2] = p[1] + 1;
			while (p[2] <= '9')
			{
				write(1, &p[0], 1);
				write(1, &p[1], 1);
				write(1, &p[2], 1);
				if (p[0] < '7')
					write(1, ", ", 2);
				p[2] = p[2] + 1;
			}
			p[1] = p[1] + 1;
		}
		p[0] = p[0] + 1;
	}
}
